import java.util.Scanner;

public class perimeter_of_rectangle {

	public static void main(String[] args) {
Scanner sc = new Scanner(System.in);
		
		 
		
		System.out.println("enter a l for perimeter of rectangle");
		int l = sc.nextInt();
		
		System.out.println("enter a w for perimeter of rectangle");
		int w = sc.nextInt();
		
			
		System.out.println("Aria of perimeter of rectangle: "+ 2*(l+w) );
	}

}
